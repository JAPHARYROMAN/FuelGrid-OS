// TestPremiumPDF_EmptyEnvelope renders a minimal (no summary, no table, no
// insights) envelope and asserts the layout still produces a valid PDF rather
// than panicking on the empty blocks.
func TestPremiumPDF_EmptyEnvelope(t *testing.T) {
	env := newEnvelope("financials", "Financial Statement", "this-month", nil)
	out, err := renderEnvelopePDF(env, LetterheadBranding{}, "", time.Time{})
	if err != nil {
		t.Fatalf("renderEnvelopePDF (empty): %v", err)
	}
	assertValidPDF(t, out)
}

// TestPremiumPDF_DeterministicForSameInput renders the same envelope twice with a
// pinned generatedAt and asserts byte-identical output — the document is
// deterministic, so the audited checksum is stable for a given report snapshot.
func TestPremiumPDF_DeterministicForSameInput(t *testing.T) {
	at := time.Date(2026, 6, 14, 9, 30, 0, 0, time.UTC)
	a, err := renderEnvelopePDF(sampleEnvelope(), LetterheadBranding{}, "Ada Admin", at)
	if err != nil {
		t.Fatalf("render a: %v", err)
	}
	b, err := renderEnvelopePDF(sampleEnvelope(), LetterheadBranding{}, "Ada Admin", at)
	if err != nil {
		t.Fatalf("render b: %v", err)
	}
	if !bytes.Equal(a, b) {
		t.Fatal("premium PDF is not deterministic for identical input")
	}
}

// TestRenderEnvelopeCSV_CarriesDecimalStrings asserts the CSV renderer passes the
// exact decimal money strings through verbatim (no float reformatting).
func TestRenderEnvelopeCSV_CarriesDecimalStrings(t *testing.T) {
	out := string(renderEnvelopeCSV(sampleEnvelope()))
	for _, want := range []string{"12345678.90", "-1500.00", "987654.32", "Daily Station Close"} {
		if !contains(out, want) {
			t.Fatalf("CSV missing exact value %q\n%s", want, out)
		}
	}
}

// contains is a tiny strings.Contains shim kept local to avoid widening imports.
func contains(haystack, needle string) bool {
	return bytes.Contains([]byte(haystack), []byte(needle))
}
