// sampleEnvelope builds a representative rendered ReportEnvelope exercising every
// premium-PDF block: KPIs (money + count), a multi-column table with numeric and
// text columns, insights of varying severity, data-quality caveats, and
// recommended actions. Money values are exact decimal strings.
func sampleEnvelope() ReportEnvelope {
	sid := "11111111-1111-1111-1111-111111111111"
	env := newEnvelope("station-close", "Daily Station Close — Mikocheni", "2026-05", &sid)
	env.FiltersUsed["station_id"] = sid
	env.FiltersUsed["period"] = "2026-05"
	env.Summary = []summaryMetric{
		{Label: "Sales value", Value: "12345678.90", Unit: "TZS"},
		{Label: "Net revenue", Value: "11000000.00", Unit: "TZS"},
		{Label: "Margin", Value: "987654.32", Unit: "TZS"},
		{Label: "Total tendered", Value: "12000000.00", Unit: "TZS"},
		{Label: "Cash variance", Value: "-1500.00", Unit: "TZS"},
		{Label: "Open exceptions", Value: "2", Unit: "count"},
	}
	env.Table.Columns = []string{"business_date", "status", "gross", "net", "margin", "tendered", "cash_variance"}
	env.Table.Rows = [][]string{
		{"2026-05-30", "locked", "12345678.90", "11000000.00", "987654.32", "12000000.00", "-1500.00"},
		{"2026-05-29", "locked", "11111111.10", "10000000.00", "900000.00", "11050000.00", "250.00"},
	}
	env.Insights = []reporting.Insight{
		{Severity: reporting.SeverityWarning, Message: "Cash variance exceeded tolerance on the latest day.", RecommendedAction: "Recount the drawer and reconcile the shortfall."},
		{Severity: reporting.SeverityInfo, Message: "Snapshot covers 2 recent revenue days."},
	}
	env.DataQuality = []dataQualityItem{
		{Level: "warning", Message: "One shift remained unclosed when this report was generated."},
	}
	env.RecommendedActions = []string{"Recount the drawer and reconcile the shortfall."}
	return env
}

// TestPremiumPDF_BrandingAbsent renders the premium envelope PDF with the EMPTY
// (FuelGrid-only) letterhead — the common path when a tenant has no
// tenant_branding row — and asserts it renders without panic into a valid PDF.
func TestPremiumPDF_BrandingAbsent(t *testing.T) {
	at := time.Date(2026, 6, 14, 9, 30, 0, 0, time.UTC)
	out, err := renderEnvelopePDF(sampleEnvelope(), LetterheadBranding{}, "Ada Admin", at)
	if err != nil {
		t.Fatalf("renderEnvelopePDF (no branding): %v", err)
	}
	assertValidPDF(t, out)
}

